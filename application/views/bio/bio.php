<p>
  A Sociopata iniciou seus projetos em 2008 em Agudos/Bauru (interior de São Paulo).
  O nome do grupo suscita argumentação, dando espaço à letras que trilham uma temática
  comportamental e/ou social com críticas direcionadas à manipulações e explorações
  humanas. Integraram a banda os bateristas Juninho (Artigo DZ9?), Maic (Psicultura)
  e Gersinho (The Almighty Devildogs), e atualmente a banda é formada por Kleber
  Cabrera (voz), Thiago Braga (guitarra), Birão Spoldari (baixo), Leopoldo Sanches
  (guitarra) e Gustavo Panurge (bateria).
</p>

<p>
  Com sua formação vinda de bandas com sonoridades diferentes, sua música é caracterizada
  pelo peso e pelo som progressivo, somada à influência vinda de Faith No More,
  Napalm Death, Meshuggah, Cripple Bastards, Pink Floyd, Pantera, Sepultura, etc.
  A proposta de tocar músicas próprias desde o começo vem divulgando o nome da banda
  em cidades do centro-oeste paulista e da capital, com apresentações ao lado de bandas
  como Necrofobia, Mordeth, Clawn, Bonequinho e Irmã Talitha.
</p>